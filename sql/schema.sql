-- Oracle schema for SK Crown Invoice & Quotation System

CREATE TABLE TEMP_DOCUMENTS (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_name VARCHAR2(255) NOT NULL,
  items_json CLOB NOT NULL,
  total_amount NUMBER(12,2) NOT NULL,
  type VARCHAR2(20) CHECK (type IN ('quotation', 'invoice')) NOT NULL,
  created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE CLIENT_HISTORY (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_name VARCHAR2(255) NOT NULL,
  total_amount NUMBER(12,2) NOT NULL,
  created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE INDEX IDX_TEMP_DOC_CREATED_AT ON TEMP_DOCUMENTS (created_at);
CREATE INDEX IDX_HISTORY_CREATED_AT ON CLIENT_HISTORY (created_at);
